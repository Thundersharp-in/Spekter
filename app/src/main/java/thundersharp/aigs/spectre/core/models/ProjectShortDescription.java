package thundersharp.aigs.spectre.core.models;

public class ProjectShortDescription {

    public String name;
    public String description;
    public String category;
    public String id;

    public ProjectShortDescription(String name, String description, String category, String id) {
        this.name = name;
        this.description = description;
        this.category = category;
        this.id = id;
    }
}
